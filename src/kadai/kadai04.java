package kadai;

public class kadai04 {

	public static void main(String[] args) {
		int x = 66;
		if (x >= 80){
			System.out.println("優");
		} else if (x >= 70) {
			System.out.println("良");
		} else if (x >= 60) {
			System.out.println("可");
		} else if (x >= 0) {
			System.out.println("不可");
		} else {
			System.out.println("不正な点数です。");
		}
	}

}
